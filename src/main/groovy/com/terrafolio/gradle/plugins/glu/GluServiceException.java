package com.terrafolio.gradle.plugins.glu;

public class GluServiceException extends Exception {

	public GluServiceException() { }

	public GluServiceException(String arg0) {
		super(arg0);
	}

	public GluServiceException(Throwable arg0) {
		super(arg0);
	}

	public GluServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
